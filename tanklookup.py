def tanklookup(tank):
    tank = tank.lower()
    result = next((tier[tank] for tier in alltanks if tank in tier), None)

    if result:
        return "https://tanks.gg/tank/" + result
    result = [tier[key] for tier in alltanks for key in tier if tank in key]
    if result:
        result = list(set(result))
        if len(result) > 5:
            return "more than 5 tanks found; refine search"
        elif 5 >= len(result) > 1:
            answer = str(len(result)) + " tanks found:"
            for t in result:
                answer += "\nhttps://tanks.gg/tank/" + t
            return answer
        return "https://tanks.gg/tank/" + result[0]

    return "tank not found"


def tankcompare(args):
    tanks = []
    for arg in args:
        arg = arg.lower()
        result = next((tier[arg] for tier in alltanks if arg in tier), None)
        if result:
            tanks.append(result)

    if tanks:
        answer = "https://tanks.gg/compare/"
        for i, tank in enumerate(tanks):
            if i == 0:
                answer += tank + "?t="
            elif i == 1:
                answer += tank
            else:
                answer += "~" + tank
        return answer

    return "No tanks found"
